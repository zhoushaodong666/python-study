# 变量名命名的规范
# 变量名只可以包含 字母、数字、下划线。并且只有 字母、下划线可以开头
# 例如 name1 _name是正确的命名, 2name则是错误的命名
# 变量名不能包含空格 student name是错误 我们可以用下划线分隔 student_name
# 变量名不能使用python的一些关键字 就像 print 函数  这是python的保留字


# 我们用一个变量message来存 hello world!!
message = "hello world!!"
print(message)

# 变量变量 顾名思义变量是可以变的 即变量的值是可以改变的
message = "hello python!!"
print(message)

